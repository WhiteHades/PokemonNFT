<script>
    const pokemonList = <?php echo json_encode($pokemons); ?>;
    function displayImage(pokemonName) {
        const imageUrl = pokemonList[pokemonName];
        const img = document.getElementById("picture");
        const hiddenInput = document.getElementById("hiddenPokemon")
        if (imageUrl) {
            img.src = imageUrl;
            img.style.display = "block";
            hiddenInput.value = pokemonName;
        } else {
            img.style.display = "none";
            hiddenInput.value = "";
            console.error("Image URL not found for:", pokemonName);
        }
    }
    window.onload = function() {
        var initialPokemon = document.getElementById("pokemon").value;
        if (initialPokemon in pokemonList) displayImage(initialPokemon);
    };
